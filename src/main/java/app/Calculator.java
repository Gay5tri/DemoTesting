package app;

public class Calculator {

	public static int add(int i,int j)
	{
		int result=i+j;
		return result;
	}
	
	public static int substract(int i,int j)
	{
		int result=i-j;
		return result;
	}
	
	public static int multiply(int i,int j)
	{
		int result=i*j;
		return result;
	}
	public static int divide(int i,int j)
	{
		int result=i/j;
		return result;
	}
}
